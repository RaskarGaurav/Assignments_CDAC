package reflectionExample;

public class Math {
	public void add(int i , int j) {
		System.out.println(i+j);
	}
	public void sub(int i , int j) {
		System.out.println(i-j);
	}
	public void mul(int i , int j) {
		System.out.println(i*j);
	}
	public void div(int i , int j) {
		System.out.println(i/j);
	}
}
